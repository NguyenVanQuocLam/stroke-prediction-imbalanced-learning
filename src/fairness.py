import numpy as np
from sklearn.metrics import confusion_matrix, roc_auc_score


def fairness_check(model, X_test, y_test, group_col):

    results = {}

    groups = X_test[group_col].unique()

    for g in groups:
        idx = X_test[group_col] == g
        X_g = X_test[idx]
        y_g = y_test[idx]

        y_pred = model.predict(X_g)
        y_prob = model.predict_proba(X_g)[:, 1]

        cm = confusion_matrix(y_g, y_pred)

        if len(cm) == 2:
            tn, fp, fn, tp = cm.ravel()
            tpr = tp / (tp + fn) if (tp + fn) > 0 else 0
            fpr = fp / (fp + tn) if (fp + tn) > 0 else 0
        else:
            tpr, fpr = 0, 0

        results[g] = {
            "support": len(y_g),
            "roc_auc": roc_auc_score(y_g, y_prob),
            "tpr": tpr,
            "fpr": fpr
        }

    return results